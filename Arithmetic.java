class Arithmetic {

   	public static void main(String[] args) {
		int x = 22;
        
		int y = 40;
        
        
		int sum = x+y;
		int mul = x*y;       
		int sub = x-y;
		int div	= x/y;
		int remainder = x%y;

	        
		System.out.println("Sum:" + sum);
       
		System.out.println("product:" + mul);
        
		System.out.println("Diff:" + sub);
		System.out.println("div:" + div);
		System.out.println("Remainder: " + remainder);
    }
}
